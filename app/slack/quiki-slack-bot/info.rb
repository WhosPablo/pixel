module QuikiBot
  INFO = <<-EOS
I am your friendly Quiki Bot, here to help.

General
-------
auto answer on     - Automatically determine which questions to answer
auto answer off    - Turn of automatic discovery of questions
help               - get this helpful message


Slash commands
-----

/q                 - Have Quiki search for your question and ask it if it cannot find it
/a                 - Answer the last Quiki question asked

  EOS
end

