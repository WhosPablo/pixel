  # POST /resource
  def create
    #Determine whether there is currently a ghost user with that email, and update if there is
    potential_ghost_user = User.find_or_create_by(email: params[:user][:email].downcase)
    if potential_ghost_user and potential_ghost_user.is_ghost_user
      sign_in potential_ghost_user.turn_ghost_user_to_real_user(params)
      redirect_to root_path
    else
      super
    end
  end

